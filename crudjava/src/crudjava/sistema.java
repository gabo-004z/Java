         public void mostrardatos(){
             String[] titulos={"ID PRODUCTO","NOMBRE DEL PRODUCTO","DESCRIPCION","BODEGA","PRECIO DE VENTA","TALLA"};
             String[] registros=new String[7];
             DefaultTableModel Modelo=new DefaultTableModel(null,titulos);
             String SQL="SELECT * FROM  alumnos";
             try{
                 Statement st=(Statement)con.createStatement();
                 ResultSet ra=st.executeQuery(SQL);//libreria sql
                 while(ra.next()){
                     registros[0]=ra.getString("idalumnos");//NOMBRE IGUAL A LA BASE DE DATOS
                     registros[1]=ra.getString("nombre");
                     registros[2]=ra.getString("apellidos");
                     registros[3]=ra.getString("materia");
                     registros[4]=ra.getString("calificacion");
                     registros[5]=ra.getString("estatus");
                     
                     Modelo.addRow(registros);
                     
                 }
              tablaAumnos.setModel(Modelo);//COMO SE LLAMA LA TABLA QUE SE ESTA UTILIZANDO
                 
             }catch(Exception e){
                  JOptionPane.showMessageDialog(null, "error de conexion  "+ e.getMessage());
                 
             }
                
    
}
