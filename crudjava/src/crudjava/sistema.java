    public void insertarDatos(){
        try{
            String SQL="insert into alumnos (nombre,apellidos,materia,calificacion,estatus) values (?,?,?,?,?)";
            PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1,txtNombre.getText());
            pst.setString(2,txtApellido.getText());
            int selecionado=cdMateria.getSelectedIndex();
            pst.setString(3,cdMateria.getItemAt(selecionado));
            pst.setDouble(4,Double.parseDouble(txtCalificacion.getText()));//porque es doble
            int selecionado1=cdEstatus.getSelectedIndex();
            pst.setString(5,cdEstatus.getItemAt(selecionado1));
            
            pst.execute();
             JOptionPane.showMessageDialog(null, "Registro exitoso");

            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ereor Registro"+e.getMessage());
            
        }
    }
    
     public void ActulizarDatos(){
        try{
            String SQL="UPDATE alumnos SET nombre=?,apellidos=?,materia=?,calificacion=?,estatus=? where idalumnos=?";
            int filaseleccionado=tablaAumnos.getSelectedRow();
            String dao=(String)tablaAumnos.getValueAt(filaseleccionado, 0);
            
            PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1,txtNombre.getText());
            pst.setString(2,txtApellido.getText());
            int selecionado=cdMateria.getSelectedIndex();
            pst.setString(3,cdMateria.getItemAt(selecionado));
            pst.setDouble(4,Double.parseDouble(txtCalificacion.getText()));//porque es doble
            int selecionado1=cdEstatus.getSelectedIndex();
            pst.setString(5,cdEstatus.getItemAt(selecionado1));
            
            pst.setString(6, dao);
            pst.executeUpdate();//PARA ACTUALIZACION
            
           
             JOptionPane.showMessageDialog(null, "ediccion exitosa");

            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error  de ediccion "+e.getMessage());
            
        }
        
    }
     public void eliminarregistro(){
         int filaseleccionado=tablaAumnos.getSelectedRow();
         try{
             
             String SQL="DELETE FROM alumnos WHERE idalumnos="+tablaAumnos.getValueAt(filaseleccionado, 0);
             Statement st=(Statement) con.createStatement();
             int n=st.executeUpdate(SQL);
             if(n>=0){
                 JOptionPane.showMessageDialog(null, "registro eliminado");
             }
         }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error  de eliminacion "+e.getMessage());
            
        }
    
}
